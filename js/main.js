var replyLinks = document.querySelectorAll('.replyLink');
var replyContainers = document.querySelectorAll('.replyContainer');

// Add click event listeners to each reply link
replyLinks.forEach(function(replyLink, index) {
    replyLink.addEventListener('click', function() {
        // Toggle visibility of corresponding reply container
        var replyContainer = replyContainers[index];
        if (replyContainer.style.display === 'none') {
            replyContainer.style.display = 'block';
        } else {
            replyContainer.style.display = 'none';
        }
    });
});

// Optionally, you can add click event listeners to cancel buttons within each container to hide them
var cancelButtons = document.querySelectorAll('.cancelButton');
cancelButtons.forEach(function(cancelButton) {
    cancelButton.addEventListener('click', function() {
        this.closest('.replyContainer').style.display = 'none';
    });
});
